package com.gdgm.stu.pojo;
//教师表实体类
public class teacher {
	private String teacher_no;//工号
	private String teacher_password;//登录密码
	private String teacher_name;//姓名
	private String teacher_id;//身份证号码
	private String teacher_sex;//性别
	private String teacher_bir;//出生日期
	private String teacher_phone;//联系电话
	private String teacher_home;//联系地址
	public String getTeacher_no() {
		return teacher_no;
	}
	public void setTeacher_no(String teacher_no) {
		this.teacher_no = teacher_no;
	}
	public String getTeacher_name() {
		return teacher_name;
	}
	public void setTeacher_name(String teacher_name) {
		this.teacher_name = teacher_name;
	}
	public String getTeacher_id() {
		return teacher_id;
	}
	public void setTeacher_id(String teacher_id) {
		this.teacher_id = teacher_id;
	}
	public String getTeacher_sex() {
		return teacher_sex;
	}
	public void setTeacher_sex(String teacher_sex) {
		this.teacher_sex = teacher_sex;
	}
	public String getTeacher_bir() {
		return teacher_bir;
	}
	public void setTeacher_bir(String teacher_bir) {
		this.teacher_bir = teacher_bir;
	}
	public String getTeacher_phone() {
		return teacher_phone;
	}
	public void setTeacher_phone(String teacher_phone) {
		this.teacher_phone = teacher_phone;
	}
	public String getTeacher_home() {
		return teacher_home;
	}
	public void setTeacher_home(String teacher_home) {
		this.teacher_home = teacher_home;
	}
	public String getTeacher_password() {
		return teacher_password;
	}
	public void setTeacher_password(String teacher_password) {
		this.teacher_password = teacher_password;
	}
	
}
